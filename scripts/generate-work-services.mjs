import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "../public/work/Services");

const FOLDER_TO_SLUG = {
  "alu windows": "aluminium-windows",
  "sliding doors": "sliding-doors",
  "glass partiton": "glass-partitions",
  "acp clladding": "acp-cladding",
  "Structural Glazing": "structural-glazing",
  "office partirons": "office-partitions",
  Interior: "interior-work",
};

const SLUG_TITLES = {
  "aluminium-windows": "Aluminium Windows",
  "sliding-doors": "Sliding Doors",
  "glass-partitions": "Glass Partitions",
  "acp-cladding": "ACP Cladding",
  "structural-glazing": "Structural Glazing",
  "office-partitions": "Office Partitions",
  "interior-work": "Interior Work",
};

function publicPath(folder, file) {
  const segments = ["work", "Services", folder, file].map((segment) =>
    encodeURIComponent(segment),
  );
  return `/${segments.join("/")}`;
}

function buildServiceImages() {
  const imagesBySlug = {};

  for (const [folder, slug] of Object.entries(FOLDER_TO_SLUG)) {
    const folderPath = path.join(root, folder);

    if (!fs.existsSync(folderPath)) {
      throw new Error(`Missing service folder: ${folder}`);
    }

    const files = fs
      .readdirSync(folderPath)
      .filter((f) => /\.(jpe?g|png|webp)$/i.test(f))
      .sort();

    if (files.length === 0) {
      throw new Error(`No images found in folder: ${folder}`);
    }

    const title = SLUG_TITLES[slug];
    const gallery = files.map((file, index) => ({
      src: publicPath(folder, file),
      alt: `${title} — installation photo ${index + 1}`,
    }));

    imagesBySlug[slug] = {
      hero: gallery[0].src,
      heroAlt: `${title} installation by Shree Chamunda Aluminium`,
      gallery,
    };
  }

  return imagesBySlug;
}

const serviceImages = buildServiceImages();

const output = `/* eslint-disable */
// Auto-generated by scripts/generate-work-services.mjs — do not edit manually.
// Re-run: node scripts/generate-work-services.mjs

import type { ServiceGalleryImage, ServiceSlug } from "@/types/services";

export type WorkServiceImages = {
  hero: string;
  heroAlt: string;
  gallery: ServiceGalleryImage[];
};

export const WORK_SERVICE_IMAGES: Record<ServiceSlug, WorkServiceImages> = ${JSON.stringify(serviceImages, null, 2)};
`;

const outPath = path.join(
  __dirname,
  "../constants/work-services-images.generated.ts",
);
fs.writeFileSync(outPath, output, "utf8");

const totalImages = Object.values(serviceImages).reduce(
  (count, item) => count + item.gallery.length,
  0,
);

console.log(
  `Generated ${Object.keys(serviceImages).length} services (${totalImages} images) -> constants/work-services-images.generated.ts`,
);
